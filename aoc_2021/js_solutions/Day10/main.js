function completionScore(line){
  
  var openers = ["(","[","{","<"];
  var closers = [")","]","}",">"];
  
  var openBrackets = [];
  
  //Generate a list of open brackets
  for(var a = 0;a < line.length;a ++){
    
    if(openers.includes(line.charAt(a))){
      
      openBrackets.push(line.charAt(a));
      
    }else if(closers.includes(line.charAt(a))){
      
      for(var b = 0;b < 4;b ++){
        
        if(line.charAt(a) == closers[b]){
          
          if(openBrackets[openBrackets.length-1] == openers[b]){
            
            openBrackets.pop();
            b = 4;
            
          }
          
        }
        
      }
      
    }else{
      console.log("Error");
    }
    
  }
  
  var score = 0;
  
  //Loop back through the list of open brackets, updating the score as necessary
  for(var a = openBrackets.length-1;a >= 0;a --){
    
    for(var b = 0;b < 4;b ++){
      
      if(openBrackets[a] == openers[b]){
        
        score *= 5;
        score += b+1;
        b = 4;
        
      }
      
    }
    
  }
  
  return score;
  
}

var completionScores = [];

for(var l = 0;l < lines.length;l ++){
  
  if(syntaxErrorScore(lines[l]) == 0){
    
    completionScores.push(completionScore(lines[l]));
    
  }
  
}

