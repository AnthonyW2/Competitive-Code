var lines = input.split("\n");

var part1sum = 0;

function syntaxErrorScore(line){
  
  ///console.log("line:",line);
  
  var openers = ["(","[","{","<"];
  var closers = [")","]","}",">"];
  var scores = [3,57,1197,25137];
  
  var openBrackets = [];
  
  for(var a = 0;a < 4;a ++){
    if(line.charAt(0) == closers[a]){
      return scores[a];
    }
  }
  
  for(var a = 0;a < line.length;a ++){
    
    if(openers.includes(line.charAt(a))){
      
      openBrackets.push(line.charAt(a));
      
    }else if(closers.includes(line.charAt(a))){
      
      for(var b = 0;b < 4;b ++){
        
        if(line.charAt(a) == closers[b]){
          
          if(openBrackets[openBrackets.length-1] != openers[b]){
            
            ///console.log("score:",scores[b]);
            ///console.log("pos:",a);
            
            return scores[b];
            
          }else{
            
            openBrackets.pop();
            b = 4;
            
          }
          
        }
        
      }
      
    }else{
      console.log("Error");
    }
    
  }
  
  ///console.log("No syntax error found");
  
  return 0;
  
}

for(var l = 0;l < lines.length;l ++){
  
  part1sum += syntaxErrorScore(lines[l]);
  
}

console.log("Part 1:",part1sum);


