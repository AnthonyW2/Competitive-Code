//Define a long rope object
class LongRope {
  
  constructor(len){
    
    //Store the coordinates of all segments
    this.len = len;
    this.sx = [];
    this.sy = [];
    for(var s = 0;s < len;s ++){
      this.sx[s] = 0;
      this.sy[s] = 0;
    }
    
    this.tailHist = [[0,0]];
    
  }
  
  move(dir){
    
    //Move the head
    if(dir == "R"){
      //this.moveSegment(0, this.sx[0]+1, this.sy[0]);
      this.shiftSegment(0, 1, 0);
    }else if(dir == "L"){
      //this.moveSegment(0, this.sx[0]-1, this.sy[0]);
      this.shiftSegment(0, -1, 0);
    }else if(dir == "D"){
      //this.moveSegment(0, this.sx[0], this.sy[0]+1);
      this.shiftSegment(0, 0, 1);
    }else if(dir == "U"){
      //this.moveSegment(0, this.sx[0], this.sy[0]-1);
      this.shiftSegment(0, 0, -1);
    }
    
  }
  
  shiftSegment(seg, x, y){
    
    //Store the previous segment coordinates
    var px = this.sx[seg];
    var py = this.sy[seg];
    
    this.sx[seg] += x;
    this.sy[seg] += y;
    
    //Check if this is the last segment
    if(seg == this.len-1){
      
      //Record the tail's position
      this.tailHist.push([this.sx[seg],this.sy[seg]]);
      
    }else{
      
      //If the next segment is no longer adjacent to this segment, move it
      if(Math.abs(this.sx[seg+1] - this.sx[seg]) > 1 || Math.abs(this.sy[seg+1] - this.sy[seg]) > 1){
        
        //Check if this segment moved diagonally
        if(Math.abs(x) == 1 && Math.abs(y) == 1){
          
          //Check if the next segment should move orthogonally or diagonally towards this segment
          if(this.sx[seg+1] == this.sx[seg]){
            //Move the next segment towards this segment along the y-axis only
            this.shiftSegment(seg+1, 0, y);
            
          }else if(this.sy[seg+1] == this.sy[seg]){
            //Move the next segment towards this segment along the x-axis only
            this.shiftSegment(seg+1, x, 0);
            
          }else{
            //Move the next segment the same way that this segment moved
            this.shiftSegment(seg+1, x, y);
          }
          
        }else{
          
          //Not a diagonal move, so move the next segment to this segment's previous coordinates
          this.shiftSegment(seg+1, px-this.sx[seg+1], py-this.sy[seg+1]);
          
        }
        
      }
      
    }
    
  }
  
  moveSegment(seg, x, y){
    
    //Store the previous segment coordinates
    var px = this.sx[seg];
    var py = this.sy[seg];
    
    this.sx[seg] = x;
    this.sy[seg] = y;
    
    if(seg < this.len-1){
      
      //If the next segment is no longer adjacent to this segment, move it to this segment's previous coordinates
      if(Math.abs(this.sx[seg+1] - this.sx[seg]) > 1 || Math.abs(this.sy[seg+1] - this.sy[seg]) > 1){
        this.moveSegment(seg+1, px, py);
      }
      
    }else{
      
      //Record the tail's position
      this.tailHist.push([this.sx[seg],this.sy[seg]]);
      
    }
    
  }
  
  debugRender(rx, ry){
    
    var output = "";
    
    for(var y = -ry;y <= ry;y ++){
      
      for(var x = -rx;x <= rx;x ++){
        
        var symbol = ".";
        if(x == 0 && y == 0){
          symbol = "s";
        }
        
        for(var s = this.len-1;s >= 0;s --){
          
          if(x == this.sx[s] && y == this.sy[s]){
            symbol = s.toString();
          }
          
        }
        
        output += symbol;
        
      }
      
      output += "\n";
      
    }
    
    return output;
    
  }
  
}


